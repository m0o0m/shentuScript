function exchangex(dlg, player, silver)
	silver = tonumber(silver)
	silver = math.floor(silver)
	if silver == nil then
		showmsg(player, "请输入纯数字！")
	return end
	
	if silver <= 0 then
		showmsg(player, "请输入大于0的正整数！")
	return end
	
	local online_time = lualib:GetInt(player, "online_time")
	if online_time < silver * point then
		showmsg(player, "您的在线时间不足。")
	return end
	
	if not lualib:SetInt(player, "online_time", online_time - silver * point) then
		showmsg(player, "扣除在线时间失败。")
	return end
	
	local xiuxian = lualib:GetInt(player, "xiuxian") + silver
	lualib:SetInt(player, "xiuxian", xiuxian)
	lualib:SysWarnMsg(player, "恭喜您成功兑换"..silver.."点修仙值！")

	check(player)
end


