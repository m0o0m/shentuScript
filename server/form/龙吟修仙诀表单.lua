function addexercise(player, id)
	local no = lualib:GetTempInt(player, "color")
	lualib:SetTempInt(player, "color", no + 1)
	lualib:SetStr(player, "_tc_", color[no % 7])
	lualib:NotifyVar(player, "_tc_")
	------------------------------------------------
	local continue = lualib:GetTempInt(player, "continue") + 2
	if continue % second == 0 then
		local lv = lualib:GetInt(player, "exerciselv")
		local value = lualib:GetInt(player, "exercisexp")
		if value >= exercise_t[lv].max then return end
		local respct = lualib:GetInt(player, "respct")
		local addval = math.ceil((exercise_t[lv].res + respct) * exercise_t[lv].max / 100)
		lualib:SetInt(player, "exercisexp", math.min(value + addval, exercise_t[lv].max))
		lualib:SysTipsMsg(player, "龙吟之力+"..addval)
		update(player)
	end
	lualib:SetTempInt(player, "continue", continue)
end

function update(player)
	local respct = lualib:GetInt(player, "respct")
	local lv = lualib:GetInt(player, "exerciselv")
	local value = lualib:GetInt(player, "exercisexp")
	local max = exercise_t[lv] and exercise_t[lv].max or 0
	local spd = exercise_t[lv] and exercise_t[lv].res + respct or 0
	local jmpct = exercise_t[lv] and exercise_t[lv].spd or 0
	local script = [[
		if Rage ~= nil then
			Rage.Exercise(]]..lv..[[, ]]..value..[[, ]]..max..[[, ]]..spd..[[, ]]..jmpct..[[);
		end
	]]
	lualib:ShowFormWithContent(player, "脚本表单", script)
	return ""
end

