function GameSettingBasic.UIInit(_Parent)
	GameSettingBasic.WndHandle = _Parent
	for _Idx = 0, 18 do 
		local _GUIIDString = "Check".._Idx
		local _GUIHandle = GUI:WndFindChildM(GameSettingBasic.WndHandle, _GUIIDString)
		if 0 ~= _GUIHandle then
			if _Idx < #GameSettingBasic.CheckName then
				GUI:CheckBoxSetText(_GUIHandle, GameSettingBasic.CheckName[_Idx + 1])
				GUI:WndSetTextOffset(_GUIHandle, 20, 0)
				GUI:WndRegistScript(_GUIHandle, RDWnd2DCheckCL_check_change, "GameSettingBasic.OnCheckChange")
				GUI:WndSetParam(_GUIHandle, _Idx)
			end
		end
	end
end
