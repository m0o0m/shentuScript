function GameSettingBasic.OnCheckChange(_Handle,_Param)
	local _WndParam = GUI:WndGetParam(_Handle)
	local _Check = GUI:CheckBoxGetCheck(_Handle)
	CL:SetBasicSetting(_WndParam,_Check)
end
