function addproperty(player,monster,level)
	
	local job = lualib:Job(player)
	i=20
	monster = tonumber(monster)
	level = tonumber(level)
	local  boss_points = tonumber(lualib:GetInt(player,"boss_points"))
	local  boss_level =tonumber( lualib:GetInt(player,"boss_level"))
	if level == boss_level + 1 then 
		if boss_points >= points_tab[monster][level][4] then
			boss_points = boss_points-points_tab[monster][level][4]
			lualib:SetInt(player,"boss_points",boss_points)
			for k , v in pairs(points_tab[monster][level][job]) do 
				if i == 30 then 
					lualib:SetDynamicAttr(player, 9, tonumber(k), tonumber(v))
				else 
					lualib:SetDynamicAttr(player, i, tonumber(k), tonumber(v))
				end
				i = i + 1
			end 
			local  num = 0 
			if level == 10 then 
				lualib:SetInt(player,"boss_level",0)
				if  monster == 4 then 
					num = 0
				    lualib:SetInt(player,"boss_level",10)
					lualib:SetInt(player,"boss_name",4)
				else
					lualib:SetInt(player,"boss_name",monster+1)
					num = points_tab[monster+1][1][4] 
				end
			else 	
				lualib:SetInt(player,"boss_name",monster)
				lualib:SetInt(player,"boss_level",level)
				num=points_tab[monster][level+1][4] 
			end
			local numing = numVisible(player)
			lualib:ShowFormWithContent(player,"脚本表单",mon_tab[monster]..".Img("..level..","..num..","..boss_points..");Tzboss.numVisible("..numing..")")
			xianshi(player,"123")
		else 
			lualib:ShowFormWithContent(player,"脚本表单",mon_tab[monster]..".msg()")
		end	 
	elseif level > boss_level + 1 then 
		lualib:ShowFormWithContent(player,"脚本表单", mon_tab[monster]..".msg2()")
	end
	return ""
	
end

