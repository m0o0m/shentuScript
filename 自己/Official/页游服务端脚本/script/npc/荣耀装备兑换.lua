function zhuangbei_huishou_1(player,item)
	--判断装备是否在包裹中
	if lualib:Item_GetSite(player,item) == "" then
		lualib:SysMsg_SendWarnMsg(player, "回收失败！物品不存在！")
		return
	end
	
	local item_keyname = lualib:KeyName(item)
	
	if zhuangb_table[item_keyname] == nil then
		lualib:SysMsg_SendWarnMsg(player, "该装备不可被回收！")
		return
	end
	
	local lv = lualib:Equip_GetRefineLevel(player,item)
	local baoguo = 0 --需要的包裹空位数
	
	if huishou_table[lv][2] ~= nil then
		for i=1,#huishou_table[lv][2] do
			for k,v in pairs(huishou_table[lv][2]) do
				baoguo = baoguo + v[2]
			end
		end
	end

	if lualib:Player_GetBagFree(player) < baoguo then
		lualib:MsgBox(player,"包裹空位不足"..baoguo.."个！请整理以后再来！")
		return
	end
    
    local item_rongyao = 0
    local item_rongyao_paizi_count = 0
    if zhuangb_table[item_keyname] > 1000 then
        item_rongyao = math.floor(zhuangb_table[item_keyname] * huishou_table[lv][1] / 1000) + zhuangb_table[item_keyname] - 1000
    else
        item_rongyao = math.floor(zhuangb_table[item_keyname] * huishou_table[lv][1] / 1000)
    end
    if item_rongyao >= 100 then
        item_rongyao_paizi_count = math.floor(item_rongyao / 100)
        item_rongyao = item_rongyao % 100
    end
    
    local t = lualib:Item_DataRow(item_keyname)
    local sellprice = t["SellPrice"]
    local max_naijiu = t["Durability"]
    local cur_naijiu = lualib:Item_GetDurability(player, item)
    local item_price = math.floor(sellprice * cur_naijiu / max_naijiu)
    local item_isbind = lualib:Item_IsBind(player, item)
    
	if not lualib:Item_Destroy(player, item, "回收装备", player) then
		lualib:SysMsg_SendWarnMsg(player, "回收装备失败！")
		return false
	end

	lualib:SetInt(player, "jjc_rongyao",lualib:GetInt(player, "jjc_rongyao") + item_rongyao) --增加荣耀值
    local s = ""
    if item_rongyao > 0 then
        s = "回收成功：获得荣耀*"..item_rongyao
    else
        if item_isbind == true then
            s = "由于装备灵气不足，无法获得荣耀值，获得了"..item_price.."绑定金币！"
        else
            s = "由于装备灵气不足，无法获得荣耀值，获得了"..item_price.."金币！"
        end
        lualib:Player_AddGold(player, item_price, item_isbind, "荣耀回收！", player)
    end
	if huishou_table[lv][2] ~= nil then
		for k,v in pairs(huishou_table[lv][2]) do
            if item_rongyao_paizi_count > 0 then
                lualib:AddItem(player, v[1], item_rongyao_paizi_count, false,"回收装备获得", player) --给物品
                s = s .. "、"..v[1].."*"..item_rongyao_paizi_count
            else
                s = s
            end
		end
	end
	
    
	lualib:MsgBox(player,s)
	local str = 
	[[
	RDItemCtrlClearGUIData(nil, "装备回收,物品")
	RichEditClear(nil,"装备回收,文字2")
	]]
	lualib:ShowFormWithContent(player, "脚本表单", str)
end